def square_area(side):
    # accept the side from main, calculate the square area, and returns it to main
    # Area of a Square = (Side)^2 = a^2
    return side ** 2
  
    

def square_perimeter(side):
    # accept the side from main, calculate the square perimeter, and return it to main
    # Perimeter of a Square = 4 × Side = 4a
    return side * 4
    